    var cssFilter = fliter('**/*.css',{restore:ture});
    var indexHtmlFilter = fliter(['**/*','!**/index.html'],{restore:ture});

    return gulp.src('src/index.html')
        .pipe(useref())
        .pipe(jsFilter)
